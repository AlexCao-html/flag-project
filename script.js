document.querySelector("#Grand-Union-Flag").addEventListener("click", () => {
    for (var i = 0; i < document.querySelectorAll("main section").length; i++) {
        document.querySelectorAll("main section")[i].style.display = "none"
    }
    document.querySelector("#Grand-Union-Flag-Sect").style.display = "grid"
})

